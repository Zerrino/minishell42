#include "libft.h"

char	*ft_strdup(char *src)
{
	char	*dest;
	int		i;
	int		src_len;

	if (src == NULL)
		return (NULL);
	src_len = ft_strlen(src) + 1;
	dest = (char *)malloc(sizeof(char) * src_len);
	if (!dest)
		return (0);
	i = 0;
	while (src[i])
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = src[i];
	return (dest);
}
